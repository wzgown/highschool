def normalize_school_name(name: str) -> str:
    """将简称转换为完整名称"""
    return SCHOOL_NAME_MAP.get(name, name)


def generate_sql():
    """生成SQL更新语句"""
    sql_statements = []

    sql_statements.append("-- 更新2025年7个区统一招生计划数据")
    sql_statements.append("-- 生成时间: 2025-02-24")
    sql_statements.append("")

    # 1. 清理这7个区的现有数据
    sql_statements.append("-- 第一步：清理现有数据")
    district_ids = list(DISTRICT_ID_MAP.values())
    sql_statements.append(
        f"DELETE FROM ref_quota_unified_allocation_district "
        f"WHERE year = 2025 AND district_id IN ({', '.join(map(str, district_ids))});"
    )
    sql_statements.append("")

    # 2. 更新本区高中的 unified_count
    sql_statements.append("-- 第二步：更新本区高中的统一招生计划数")
    for district_name, schools in LOCAL_SCHOOLS.items():
        district_id = DISTRICT_ID_MAP[district_name]
        for school_name, unified_count in schools:
            full_name = normalize_school_name(school_name)
            sql_statements.append(
                f"UPDATE ref_admission_plan_summary "
                f"SET unified_count = {unified_count}, "
                f"updated_at = CURRENT_TIMESTAMP "
                f"WHERE year = 2025 "
                f"AND school_id = (SELECT id FROM ref_school WHERE full_name = '{full_name}') "
                f"AND district_id = {district_id};"
            )
    sql_statements.append("")

    # 3. 插入跨区招生数据到 ref_quota_unified_allocation_district
    sql_statements.append("-- 第三步：插入跨区招生数据")
    sql_statements.append(
        "INSERT INTO ref_quota_unified_allocation_district "
        "(year, school_id, school_code, district_id, quota_count, created_at, updated_at) VALUES"
    )

    values = []
    for district_name, schools in CROSS_DISTRICT_SCHOOLS.items():
        district_id = DISTRICT_ID_MAP[district_name]
        for school_name, quota_count in schools:
            if quota_count > 0:  # 跳过0名额的学校
                full_name = normalize_school_name(school_name)
                values.append(
                    f"(2025, "
                    f"(SELECT id FROM ref_school WHERE full_name = '{full_name}'), "
                    f"(SELECT code FROM ref_school WHERE full_name = '{full_name}'), "
                    f"{district_id}, {quota_count}, CURRENT_TIMESTAMP, CURRENT_TIMESTAMP)"
                )

    sql_statements.append(",\n".join(values) + ";")
    sql_statements.append("")

    # 4. 验证语句
    sql_statements.append("-- 第四步：验证数据")
    sql_statements.append(
        "SELECT d.name as district, COUNT(*) as school_count, SUM(quota_count) as total_quota "
        "FROM ref_quota_unified_allocation_district q "
        "JOIN ref_district d ON q.district_id = d.id "
        "WHERE q.year = 2025 AND q.district_id IN (" + ", ".join(map(str, district_ids)) + ") "
        "GROUP BY d.name ORDER BY d.name;"
    )

    return "\n".join(sql_statements)


if __name__ == "__main__":
    print(generate_sql())
